    public void mostrar(){
        
       
        
        DefaultTableModel templeado = new DefaultTableModel();
        
        templeado.addColumn("Id");
        templeado.addColumn("Nombre");
        templeado.addColumn("Apellido");
        templeado.addColumn("Telefono");
        templeado.addColumn("Email");
        templeado.addColumn("Sueldo");
        templeado.addColumn("Posicion");
        templeado.addColumn("Fecha_deIngreso");
        templeado.addColumn("Departamento");
        jTable1.setModel(templeado);
        
        String []datos = new String[9];
        
        try {
            ConexionDB con = new ConexionDB();
            
           String SQL = "SELECT * FROM nomina.empleado ";
            
            con.ConectarBasedeDatos();
            con.resultado = con.sentencia.executeQuery(SQL);
            
            while (con.resultado.next()){
            
                datos[0] = con.resultado.getString(1);
                datos[1] = con.resultado.getString(2);
                datos[2] = con.resultado.getString(3);
                datos[3] = con.resultado.getString(4);
                datos[4] = con.resultado.getString(5);
                datos[5] = con.resultado.getString(6);
                datos[6] = con.resultado.getString(7);
                datos[7] = con.resultado.getString(8);
                datos[8] = con.resultado.getString(9);
                templeado.addRow(datos);
            
            }
          
            jTable1.setModel(templeado);
            
        } catch (SQLException e){
        
        JOptionPane.showMessageDialog(null, e + "Error en la consulta");
        }
    
    }

