    private void jLabel12MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel12MouseClicked
     
       
        
        try {
            
            
            ResultSet rs;
            
           
        
        ConexionDB con = new ConexionDB();
        String Nombre = jTextField1.getText();
        String Apellido = jTextField3.getText();
        String Telefono = jTextField4.getText();
        String Email = jTextField5.getText();
        String Sueldo = jTextField6.getText();
        String Posicion = jTextField7.getText();
        String Fecha_deInicio = jTextField8.getText();
        String id_Departamento = jTextField9.getText();
        
        
        con.ConectarBasedeDatos();
        
        if(Nombre.equals("") || Apellido.equals("") || Telefono.equals("")|| Email.equals("") || Sueldo.equals("")
                || Posicion.equals("") || Fecha_deInicio.equals("") || id_Departamento.equals("") ){
        
          JOptionPane.showMessageDialog(null, "CAMPOS VACIOS!!");
          limpiarTabla(modelo);
        
        
        }else{

        
        String SQL = "INSERT INTO `nomina`.`empleado` (Nombre, Apellido, Telefono, Email, Sueldo, Posicion, Fecha_deIngreso, Departamento)"
         + " VALUES('"+Nombre+"', '"+Apellido+"', '"+Telefono+"', '"+Email+"', '"+Sueldo+"', '"+Posicion+"', '"+Fecha_deInicio+"', '"+id_Departamento+"')"; 
        
    
        con.ConectarBasedeDatos();
        
        Statement opa = (Statement) con.conexion.createStatement();
        
        opa.executeUpdate(SQL);
        
                
        
        limpiarTabla(modelo);
        
        }
        
        

     } catch(SQLException ex){
         
        JOptionPane.showMessageDialog(null, ex.getMessage()); 
         
     
    
    
    }
    }//GEN-LAST:event_jLabel12MouseClicked

